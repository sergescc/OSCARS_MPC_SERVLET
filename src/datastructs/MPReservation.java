package datastructs;

import java.util.ArrayList;

public class MPReservation {
	
	private String MPGRI;
	private ArrayList<String> UniGris;
	/**
	 * @return the mPGRI
	 */
	public String getMPGRI() {
		return MPGRI;
	}
	/**
	 * @param mPGRI the mPGRI to set
	 */
	public void setMPGRI(String mPGRI) {
		MPGRI = mPGRI;
	}
	/**
	 * @return the uniGris
	 */
	public ArrayList<String> getUniGris() {
		return UniGris;
	}
	/**
	 * @param uniGris the uniGris to set
	 */
	public void setUniGris(ArrayList<String> uniGris) {
		UniGris = uniGris;
	}
	
	

}
